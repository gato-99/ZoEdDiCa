        <p>
            El primer antecedente donde se le da carácter institucional a las Zonas Educativas aparece en la Resolución
            Nº 847-A, Gaceta Oficial Nº 34020 de fecha 02 de agosto de 1988 donde se modifica la Resolución Nº
            395 del 29-12-1980 referente al Reglamento interno del Ministerio de Educación.<br>
            La Resolución Nº 847-A Capítulo V “De las Regiones y Zonas Educativas” establece: Artículo 40
            “En cada una de las Regiones Administrativas en que se encuentra dividido el territorio nacional
            funcionará una Dirección Regional de Educación”. Artículo 41 “Se establecen las Zonas Educativas
            tanto en el Distrito Federal como en cada uno de los Estados y Territorios federales, cuya
            jurisdicción corresponderán a las que tienen sus respectivas entidades federales según la
            División Político Territorial de la República. Artículo 42 “Las Zonas Educativas se dividirán
            a su vez en Distritos Escolares.<br>
            Debido a la heterogeneidad de la organización estructural y funcional de las Zonas Educativas,
            en mayo de 1990 el Ministerio de Educación pública un documento donde se propone una nueva
            Estructura Organizativa para las Zonas Educativas basada en la necesidad de adecuarla a
            los procesos de organización, desconcentración y descentralización administrativa del sistema
            educativo contemplados en el VIII Plan de la Nación. <br>
            En octubre de 1990 el documento propuesto, es enviado a la Dirección Nacional de Desarrollo
            Administrativo de CORDIPLAN por el Despacho del Ministro de Educación para su estudio y
            análisis, el cual fue considerado procedente en marzo de 1991, estableciéndose mediante
            el Resuelto Nº 956 de fecha 15 de agosto de 1991, la nueva estructura organizativa para
            las Zonas Educativas: <br>
            En el Reglamento Interno que el Ministerio de Educación promulga mediante Resolución,
            se determinará lo concerniente a campos de competencias y determinación de atribuciones
            y normas de funcionamiento de las distintas dependencias que integren las Zonas Educativas. <br>
            En la Resolución Nº 590 del 15-04-1998 publicada en Gaceta Oficial Nº 5229 Extraordinario de
            fecha 20-05-1998, se resuelve dividir la Zona Educativa en 7 Distritos Escolares. Cada uno de
            éstos estará conformado por sectores escolares: 5 Distritos Escolares en el Municipio
            Libertador y 2 Distritos Escolares en el Departamento Vargas. <br>
            En la Resolución Nº 21 de fecha 28 de enero de 1999, publicada en Gaceta Oficial Nº 5296
            Extraordinario de fecha 28-01-1999 se dicta el Reglamento Interno del Ministerio de
            Educación, en el Capítulo VI “ De las Zonas Educativas”, Artículo 54 se establece lo
            siguiente:<br>
            “ Las Zonas Educativas son órganos desconcentrados del Ministerio de Educación,
            adscritos a la Dirección General del Ministerio, y están integradas por el Despacho del
            Director, la Coordinación de Planificación y Presupuesto, la Coordinación de Personal, la
            Coordinación de Administración y Servicios, la Coordinación de Registro, Control y
            Evaluación de Estudios, la Coordinación Académica y los Distritos Escolares”. <br>
            En la Resolución Nº 270 de fecha 22-12-1999 publicada en Gaceta Oficial Nº 5428
            Extraordinario se dicta el Reglamento Interno del Ministerio de Educación, Cultura y
            Deporte, en el Capítulo 3 “De las Zonas Educativas” Artículo 156 se establece lo siguiente:<br>
            “Las Zonas Educativas son órganos desconcentrados del Ministerio de Educación, Cultura y
            Deporte, adscritas al Vice Ministro de Asuntos Educativos y están integradas por el Despacho
            del Director, La División de Asesoría Jurídica, la División de Informática y Sistema, la
            División de Planificación y Presupuesto, la División de Personal, la División de
            Administración y Servicios, la División de Registro, Control y Evaluación de Estudios, la
            División Académica, la División de Coordinación con Entes Públicos y Privados del Sector
            Deportivos, la División de Coordinación con Entes Públicos y Privados del Sector Cultural,
            la División de Coordinación de los Distritos Escolares, y los Distritos Escolares”. <br>
            En la Resolución Nº 181 de fecha 14-05-2001 publicada en Gaceta Oficial Nº 5531 Extraordinario
            se dicta el Reglamento Interno del Ministerio de Educación, Cultura y Deporte, en el Capítulo
            IV “De las Zonas Educativas” se mantiene la misma estructura organizativa con algunas
            modificaciones en cuanto a las funciones de las diferentes divisiones. <br>
